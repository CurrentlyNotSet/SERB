    private void logInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInButtonActionPerformed
        Password.validatePassword(UsernameTextBox.getText().trim(), new String(PasswordTextBox.getPassword()));

        if(Global.activeUser != null) {
            if(Global.activeUser.activeLogIn) {
                setVisible(false);
                new MultipleLogInDialog(null);
                Global.activeUser = null;
                setVisible(true);
            } else  {
                FileService.setFilePath();
                completeValidLogIn();
            }
        } else {
            jLabel4.setForeground(Color.RED);
            jLabel4.setText("Incorrect Username/Password");
            PasswordTextBox.setText("");
            UsernameTextBox.requestFocus();
        }
    }//GEN-LAST:event_logInButtonActionPerformed

