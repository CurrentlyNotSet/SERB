    private void addListeners() {

        jLabel1.addMouseListener(new MouseListener() {

            @Override
            public void mouseClicked(MouseEvent e) {
                if(e.getClickCount() == 2) {
                    try {
                        Desktop.getDesktop().browse(new java.net.URI ("http://www.serb.state.oh.us"));
                    } catch (IOException | URISyntaxException ex) {
                        new ExceptionHandling(null, "Unable to open URL", ex);
                    }
                }
            }

            @Override
            public void mouseReleased(MouseEvent e) {}

            @Override
            public void mouseEntered(MouseEvent e) {}

            @Override
            public void mouseExited(MouseEvent e) {}

            @Override
            public void mousePressed(MouseEvent e) {}
        });

        UsernameTextBox.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent e) {
                enableLogInButton();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                enableLogInButton();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                enableLogInButton();
            }
        });

        PasswordTextBox.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent e) {
                enableLogInButton();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                enableLogInButton();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                enableLogInButton();
            }
        });
    }

    private void enableLogInButton() {
        if(!UsernameTextBox.getText().equals("") && PasswordTextBox.getPassword().length >= 6) {
            logInButton.setEnabled(true);
        } else {
            logInButton.setEnabled(false);
        }
    }

    private void completeValidLogIn() {
        dispose();
        if(Global.activeUser.passwordReset) {
            new ResetPasswordDialog(null, true, true);
        } else {
            new RootPanel();
        }
    }

