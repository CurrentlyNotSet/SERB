    private void loadComboBox() {
        List<String> courts = AppealCourt.loadAllAppealCourt();
        
        jComboBox1.removeAllItems();
        jComboBox1.addItem("");
        
        for(int i = 0; i < courts.size(); i++) {
            jComboBox1.addItem(courts.get(i));
        }
    }

